public class Persona {
    String numeroDeTeléfono;

    public Persona(String numeroDeTeléfono) {
        super();
        this.numeroDeTeléfono = numeroDeTeléfono;
    }

    public String getNumeroDeTeléfono() {
        return numeroDeTeléfono;
    }

    public void setNumeroDeTeléfono(String numeroDeTeléfono) {
        this.numeroDeTeléfono = numeroDeTeléfono;
    }
}
